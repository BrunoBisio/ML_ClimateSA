package model;

public class ClimateReport {
    
    private String dia;
    private String clima;

    public String getDia() {
        return dia;
    }

    public String getClima() {
        return clima;
    }

    public void setClima(String clima) {
        this.clima = clima;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }
}